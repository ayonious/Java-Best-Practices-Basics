package chapter4_method_overriding;




public class A 
{
	private int a;
	
	
	public A(int a) 
    {
        this.a = a;
    }
	
	public A() 
    {
        this.a = 1;
    }
	
	public int get()
	{
		return a;
	}
	
	public void set(int a) 
    {
        this.a = a;
    }
	
	
}


